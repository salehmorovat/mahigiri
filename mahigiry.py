def fisherman_project():
    try:
        # دریافت وزن ماهی به پوند از ماهیگیر
        weight_in_pounds = float(input("لطفاً وزن ماهی به پوند وارد کنید: "))

        # تبدیل وزن به کیلوگرم
        weight_in_kg = weight_in_pounds * 0.453592

        # دریافت مقدار پول از ماهیگیر
        payment = float(input("لطفاً مقدار پول را وارد کنید: "))

        # اگر مقدار پول بیشتر از یک عددی بود، جایزه داده می‌شود
        if payment > 1:
            print("ماهیگیر جایزه‌ای برای شما دارد!")
            print("قلاب یا ابزار دیگری به شما هدیه داده می‌شود.")

        # اگر مقدار پول کمتر از یک عددی بود، جریمه در نظر گرفته می‌شود
        elif payment < 1:
            fine = abs(payment)  # مقدار مثبت از پرداخت را برای جریمه در نظر بگیرید
            print(f"شما باید به عنوان جریمه ${fine} بپردازید!")

        # محاسبه مقدار ماهی که می‌توانیم با این مقدار پول بخریم
        fish_to_buy = payment / 2
        print(f"شما می‌توانید {fish_to_buy:.2f} کیلوگرم ماهی بخرید.")

    except ValueError:
        print("ورودی نامعتبر! لطفاً عدد معتبر وارد کنید.")

# اجرای تابع
fisherman_project()
